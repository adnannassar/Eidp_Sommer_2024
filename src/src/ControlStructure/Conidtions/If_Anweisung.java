package src.ControlStructure.Conidtions;

public class If_Anweisung {
    public static void main(String[] args) {
        int x = 0;
        if (x > 0) {
            System.out.println("Positive!");
        } else if (x == 0) {
            System.out.println("Neutral!");
        }
        else {
            System.out.println("Negative!");
        }
    }
}
